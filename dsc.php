   About Digital Signature Certificate (DSC)
The Information Technology Act, 2000 has provisions for use of Digital Signatures on the documents submitted in electronic form in order to ensure the security and authenticity of the documents filed electronically. This is secure and authentic way to submit a document electronically. As such, all filings done by the companies/LLPs under MCA21 e-Governance programme are required to be filed using Digital Signatures by the person authorised to sign the documents.<br><br>
            
            <h1> 1 Legal Warning:</h1><br>
You can use only the valid Digital Signatures issued to you. It is illegal to use Digital Signatures of anybody other than the one to whom it is issued.<br><br>
            
            <h1> 2 Certification Agencies:</h1><br>
Certification Agencies are appointed by the office of the Controller of Certification Agencies (CCA) under the provisions of IT Act, 2000. There are a total of eight Certification Agencies authorised by the CCA to issue Digital Signature Certificates (DSCs). The details of these Certification Agencies are available on the portal of the Ministry<br><br>
            
            <h1> 3 Class of DSCs:</h1><br>
The Ministry of Corporate Affairs has stipulated a Class-II or above category signing certificate for e-Filings under MCA21. A person who already has the specified DSC for any other application can use the same for filings under MCA21 and is not required to obtain a fresh DSC.<br><br>
            
            <h1>4 Validity of Digital Signatures:</h1><br>
The DSCs are typically issued with one year validity and two year validity. These are renewable on expiry of the period of initial issue.<br><br>
            
            <h1>5 Costing/ Pricing of Digital Signatures:</h1><br>
It includes the cost of medium (a UBS token which is a one time cost), the cost of issuance of DSC and the renewal cost after the period of validity. The company representatives and professionals required to obtain DSCs are free to procure the same from any one of the approved Certification Agencies as per the MCA portal. The issuance costs in respect of each Agency vary and are market driven.
            
